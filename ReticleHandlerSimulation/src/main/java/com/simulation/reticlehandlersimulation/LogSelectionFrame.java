    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        clearSelectedList();
        String selectedItem = jList1.getSelectedValue();
        selectedLogFileIndex = jList1.getSelectedIndex();
        updateSelectedList(selectedItem);
    }//GEN-LAST:event_jList1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jList2.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(rootPane, "Please select a log file.");
        }
        else{
            showMotionFrame();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void showMotionFrame(){
        MotionFrame motionFrame = new MotionFrame(getAllLogFiles()[selectedLogFileIndex]);
        motionFrame.setVisible(true);
        this.dispose();
    }
    
    private void clearSelectedList(){
        DefaultListModel model = new DefaultListModel();
        jList2.setModel(model);
        model.removeAllElements();
    }
    
    private void updateSelectedList(String selectedItem){
        DefaultListModel model = new DefaultListModel();
        jList2.setModel(model);
        model.addElement(selectedItem);
    }
    
